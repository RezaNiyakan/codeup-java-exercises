
public class StringExercise {
    public static void main(String[] args) {
        String message = "We don't need no education\nWe don't need no thought control";
        System.out.println(message);

        System.out.println("//////");

        String quotations = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(quotations);

        System.out.println("//////");

        String mainDrive = "In windows, the main drive is usually C:\\";
        System.out.println(mainDrive);

        System.out.println("//////");

        String backSlashes = "I can do backslashes \\, double backslashes \\\\,\n" +
                "and the amazing triple backslash \\\\\\!";
        System.out.println(backSlashes);


    }
}
